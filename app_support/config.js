const APP_KEYS = Object.keys(APP_CONFIG);
const DEFAULT_APP_KEY = "raw";

function normalizeBasePath(basePath) {
  if (!basePath) return "";
  const withLeadingSlash = basePath.startsWith("/") ? basePath : `/${basePath}`;
  return withLeadingSlash.replace(/\/+$/, "");
}

function inferBasePath(pathname = window.location.pathname) {
  const marker = "/app_support/";
  const idx = String(pathname || "").indexOf(marker);
  if (idx === -1) return "";
  return pathname.slice(0, idx);
}

function sitePath(relativePath) {
  const cleanRelative = String(relativePath || "").replace(/^\/+/, "");
  const configuredBasePath = normalizeBasePath(SITE_CONFIG && SITE_CONFIG.basePath);
  const basePath = configuredBasePath || normalizeBasePath(inferBasePath());
  return basePath ? `${basePath}/${cleanRelative}` : `/${cleanRelative}`;
}

function normalizeAppKey(value) {
  if (!value) return null;
  const cleaned = String(value).toLowerCase().replace(/[^a-z]/g, "");
  return APP_CONFIG[cleaned] ? cleaned : null;
}

function getAppKeyFromSearch(search = window.location.search) {
  const params = new URLSearchParams(search);
  const appParam = normalizeAppKey(params.get("app"));
  if (appParam) return appParam;

  for (const key of APP_KEYS) {
    if (params.has(key)) return key;
  }

  const rawSearch = String(search || "").replace(/^\?/, "");
  if (!rawSearch) return null;

  const direct = normalizeAppKey(rawSearch);
  if (direct) return direct;

  const firstToken = normalizeAppKey(rawSearch.split(/[=&]/)[0]);
  return firstToken || null;
}

function getAppConfig(search = window.location.search) {
  const key = getAppKeyFromSearch(search);
  return key ? APP_CONFIG[key] : null;
}

function getParamForApp(appOrKey) {
  const key = typeof appOrKey === "string" ? appOrKey : appOrKey && appOrKey.key;
  return key && APP_CONFIG[key] ? `?app=${key}` : "";
}

function pageUrl(fileName, appOrKey) {
  return `${sitePath(`app_support/${fileName}`)}${getParamForApp(appOrKey)}`;
}

