function setTheme(app) {
  const root = document.documentElement;
  root.style.setProperty("--accent", app.accent);
  root.style.setProperty("--accent-soft", app.accentSoft);
  root.style.setProperty("--accent-glow", app.accentGlow);
  root.style.setProperty("--brand-gradient", app.gradient);
}

function fillAppText(app) {
  document.querySelectorAll("[data-app-name]").forEach((el) => {
    el.textContent = app.name;
  });
  document.querySelectorAll("[data-app-tag]").forEach((el) => {
    el.textContent = app.tag;
  });
  document.querySelectorAll("[data-app-icon]").forEach((el) => {
    el.innerHTML = `<img class="app-logo app-logo--nav" src="${sitePath(app.iconSrc)}" alt="${app.name} app icon">`;
  });
  document.querySelectorAll("[data-support-email]").forEach((el) => {
    el.textContent = TECH_AND_FAITH.supportEmail;
  });
  document.querySelectorAll("[data-brand-name]").forEach((el) => {
    el.textContent = TECH_AND_FAITH.displayName;
  });
  document.querySelectorAll("[data-brand-legal]").forEach((el) => {
    el.textContent = TECH_AND_FAITH.legalName;
  });
}

function hydrateAppLinks() {
  APP_KEYS.forEach((key) => {
    APP_CONFIG[key].supportEmail = TECH_AND_FAITH.supportEmail;
    APP_CONFIG[key].copyright = TECH_AND_FAITH.copyright;
    APP_CONFIG[key].iconSrc = sitePath(APP_CONFIG[key].iconSrc);
  });
}

